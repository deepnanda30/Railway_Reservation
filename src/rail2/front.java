    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        rail2 ob1 = new rail2();
        
        ob1.setVisible(true);
        ob1.setSize(1300,750);
       
        ob1.display(source.getText(), dest.getText(), date.getText());
        
        String r;
        String t;

        r = source.getText();
        t = dest.getText();
        try {
            stmt = conn.createStatement();
            rs = stmt.executeQuery("SELECT  * from trains WHERE Source='" + r + "'&& Destination='" + t + "'");
            if (rs.next()) {
                ob1.display1(rs.getString("Name"), rs.getString("time"), rs.getString("SC"), rs.getString("FC"));
               
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

